// ==== 汎用クイズ描画 ====
function mountQuiz(containerId, questions, resultHtmlFn){
  const root = document.getElementById(containerId);
  if(!root) return;
  let idx = 0, score = 0;
  root.innerHTML = `
    <div class="quiz-card">
      <p class="text-[11px] font-bold text-sand-400 mb-2" data-q-progress></p>
      <p class="text-[13.5px] text-sand-800 font-bold mb-2" data-q-question></p>
      <div data-q-options></div>
      <p class="text-[12px] text-sand-500 mt-3" data-q-explain></p>
      <button class="mt-3 bg-aura-navy text-white font-bold rounded-full px-6 py-3 text-sm" style="display:none" data-q-next>次の問題へ</button>
    </div>
    <div class="mt-4" data-q-result></div>`;
  function render(){
    const q = questions[idx];
    root.querySelector('[data-q-progress]').textContent = `問題 ${idx+1} / ${questions.length}`;
    root.querySelector('[data-q-question]').textContent = q.q;
    root.querySelector('[data-q-explain]').textContent = '';
    root.querySelector('[data-q-next]').style.display = 'none';
    root.querySelector('[data-q-options]').innerHTML = q.options.map((opt,i) => `<button class="quiz-option" data-i="${i}">${opt}</button>`).join('');
    root.querySelectorAll('.quiz-option').forEach(btn => btn.addEventListener('click', onAnswer));
  }
  function onAnswer(e){
    const i = Number(e.target.dataset.i);
    const q = questions[idx];
    root.querySelectorAll('.quiz-option').forEach(b => b.disabled = true);
    if(i === q.correct){ e.target.classList.add('is-correct'); score++; }
    else { e.target.classList.add('is-wrong'); root.querySelectorAll('.quiz-option')[q.correct].classList.add('is-correct'); }
    root.querySelector('[data-q-explain]').textContent = q.explain;
    root.querySelector('[data-q-next]').style.display = 'inline-flex';
  }
  root.querySelector('[data-q-next]').addEventListener('click', () => {
    idx++;
    if(idx < questions.length) render();
    else {
      root.querySelector('.quiz-card').style.display = 'none';
      root.querySelector('[data-q-result]').innerHTML = resultHtmlFn(score, questions.length);
      markStepDoneFromQuiz();
    }
  });
  render();
}
