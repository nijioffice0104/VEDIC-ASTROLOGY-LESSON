function shuffle(arr){ return arr.map(v=>[Math.random(),v]).sort((a,b)=>a[0]-b[0]).map(v=>v[1]); }
function pickDistractors(correctValue, pool, count){
  const uniq = [...new Set(pool.filter(v => v !== correctValue))];
  return shuffle(uniq).slice(0, count);
}
function buildLagnaQuiz(person){
  const s = bySignName(person.lagna);
  const nameOpts = shuffle([s.name, ...pickDistractors(s.name, signs.map(x=>x.name), 2)]);
  const giftOpts = shuffle([s.gift, ...pickDistractors(s.gift, signs.map(x=>x.gift), 2)]);
  const shadowOpts = shuffle([s.shadow, ...pickDistractors(s.shadow, signs.map(x=>x.shadow), 2)]);
  return [
    {q:`${person.name}さんのラグナはどれ？`, options:nameOpts, correct:nameOpts.indexOf(s.name), explain:`正解は${s.name}。${s.feature}`},
    {q:`${person.name}さんが得意なことはどれ？`, options:giftOpts, correct:giftOpts.indexOf(s.gift), explain:`${person.name}さんは「${s.gift}」が得意な傾向があります。`},
    {q:`${person.name}さんがバランスを崩すとどうなりやすい？`, options:shadowOpts, correct:shadowOpts.indexOf(s.shadow), explain:`${person.name}さんは「${s.shadow}」という点に気をつけましょう。`}
  ];
}
